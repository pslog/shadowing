#!/usr/bin/env node

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const generatedDir = path.join(rootDir, "resources", "generated");
const outputPaths = [
  path.join(rootDir, "supabase", "seed.sql"),
  path.join(rootDir, "supabase", "seed.resources.sql"),
];

function sql(value) {
  if (value == null) return "null";
  if (typeof value === "number") return Number.isFinite(value) ? String(value) : "null";
  if (typeof value === "boolean") return value ? "true" : "false";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function lessonUuid(no) {
  return `00000000-0000-0000-0000-0000000b${String(no).padStart(4, "0")}`;
}

function sentenceUuid(lessonNo, sentenceNo) {
  return `00000000-0000-0000-${String(lessonNo).padStart(4, "0")}-${String(
    sentenceNo,
  ).padStart(12, "0")}`;
}

const files = readdirSync(generatedDir)
  .filter((file) => /^lesson-\d{2}\.json$/.test(file))
  .sort();
const lessons = files.map((file) =>
  JSON.parse(readFileSync(path.join(generatedDir, file), "utf8")),
);

// All resource lessons belong to this single official course.
const COURSE = {
  id: "00000000-0000-0000-0000-0000000c0001",
  title: "IT日本語 ソフトウェア開発プロジェクト",
  description:
    "ソフトウェア開発プロジェクトの現場で使う日本語を、初回訪問・キックオフから進捗報告・UAT・オンサイト終了まで順番に学ぶコース。",
  topic: "IT日本語",
  level: "N3-N2",
  accent: "#6366f1",
  image_url: "/course-covers/it-nihongo.jpg",
};

const lines = [];
lines.push("-- ============================================================================");
lines.push("--  Official resource seed generated from resources/lesson.md + resources/*.m4a");
lines.push("--  Generated by scripts/build-resource-seed-sql.mjs.");
lines.push("--  Run after supabase/schema.sql.");
lines.push("-- ============================================================================");
lines.push("");
lines.push("begin;");
lines.push("");
lines.push("-- Replace old public starter/sample lessons with the real resource set.");
lines.push("delete from public.lessons where user_id is null and is_public = true;");
lines.push("");
lines.push("-- Single official course that groups every resource lesson.");
lines.push("insert into public.courses");
lines.push("  (id, user_id, title, description, topic, level, accent, image_url, order_index, is_public)");
lines.push("values");
lines.push(
  `  (${[
    sql(COURSE.id),
    "null",
    sql(COURSE.title),
    sql(COURSE.description),
    sql(COURSE.topic),
    sql(COURSE.level),
    sql(COURSE.accent),
    sql(COURSE.image_url),
    "0",
    "true",
  ].join(", ")})`,
);
lines.push("on conflict (id) do update set");
lines.push("  title = excluded.title,");
lines.push("  description = excluded.description,");
lines.push("  topic = excluded.topic,");
lines.push("  level = excluded.level,");
lines.push("  accent = excluded.accent,");
lines.push("  image_url = excluded.image_url,");
lines.push("  is_public = excluded.is_public;");
lines.push("");
lines.push("insert into public.lessons");
lines.push("  (id, user_id, course_id, title, topic, level, duration_seconds, source_type, source_url, media_url, is_public)");
lines.push("values");
lines.push(
  lessons
    .map((lesson) => {
      const id = lessonUuid(lesson.lesson_no);
      return `  (${[
        sql(id),
        "null",
        sql(COURSE.id),
        sql(lesson.title),
        sql(lesson.topic),
        sql(lesson.level),
        "null",
        sql(lesson.source_type ?? "upload"),
        "null",
        sql(lesson.media_url),
        "true",
      ].join(", ")})`;
    })
    .join(",\n"),
);
lines.push("on conflict (id) do update set");
lines.push("  course_id = excluded.course_id,");
lines.push("  title = excluded.title,");
lines.push("  topic = excluded.topic,");
lines.push("  level = excluded.level,");
lines.push("  duration_seconds = excluded.duration_seconds,");
lines.push("  source_type = excluded.source_type,");
lines.push("  source_url = excluded.source_url,");
lines.push("  media_url = excluded.media_url,");
lines.push("  is_public = excluded.is_public;");
lines.push("");
lines.push("insert into public.lesson_sentences");
lines.push("  (id, lesson_id, order_index, ja_text, vi_translation, audio_url, audio_start, audio_end, pass_score)");
lines.push("values");

const sentenceRows = [];
for (const lesson of lessons) {
  const lessonId = lessonUuid(lesson.lesson_no);
  for (const sentence of lesson.sentences) {
    sentenceRows.push(
      `  (${[
        sql(sentenceUuid(lesson.lesson_no, sentence.order_index + 1)),
        sql(lessonId),
        sql(sentence.order_index),
        sql(sentence.ja_text),
        sql(sentence.vi_translation),
        sql(sentence.clip_url ?? null),
        sql(sentence.audio_start),
        sql(sentence.audio_end),
        sql(sentence.pass_score ?? 80),
      ].join(", ")})`,
    );
  }
}

lines.push(sentenceRows.join(",\n"));
lines.push("on conflict (id) do update set");
lines.push("  lesson_id = excluded.lesson_id,");
lines.push("  order_index = excluded.order_index,");
lines.push("  ja_text = excluded.ja_text,");
lines.push("  vi_translation = excluded.vi_translation,");
lines.push("  audio_url = excluded.audio_url,");
lines.push("  audio_start = excluded.audio_start,");
lines.push("  audio_end = excluded.audio_end,");
lines.push("  pass_score = excluded.pass_score;");
lines.push("");
lines.push("commit;");
lines.push("");

for (const outputPath of outputPaths) {
  writeFileSync(outputPath, lines.join("\n"), "utf8");
}
console.log(
  JSON.stringify(
    {
      output: outputPaths.map((outputPath) => path.relative(rootDir, outputPath)),
      lessons: lessons.length,
      sentences: sentenceRows.length,
    },
    null,
    2,
  ),
);
